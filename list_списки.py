# Методы списков

# аналог пуша - append
list2 = []
list2.append('hi')
list2.append('lol')
print(list2)

# pop - удаление элементов списка. без аргументов удалит последний
print(list2.pop(0))

# сортировка - sort

numbers = [1, 15, 20, 3, 4]
numbers.sort()
